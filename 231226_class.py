'''표준 입력함수 input()'''

# 사용 형식 --> 변수명 = input('문자열')
user_name = input('당신의 이름을 입력하세요: ')
user_bloodType = input('당신의 혈액형을 입력하세요: ')
print("이름:",user_name,"/ 혈액형:",user_bloodType,'\n')

year = input('이번 년도를 입력하세요: ')
month = input('이번 달을 입력하세요: ')
print("년도:",year,"/ 달:",month,'\n')

# 정수 2개를 입력받아 합을 구하는 예제(잘못된 예시)
num1 = input('첫 번째 숫자를 입력하세요 : ')
num2 = input('두 번째 숫자를 입력하세요 : ')
num3 = num1 + num2
print("두수의 합 :",num3,'\n')   # 30 + 50 = 3050 과 같이 문자열 나열로 출력
# 즉 input으로 받은 숫자 또한 문자열로 변수에 저장

# 정수 2개를 입력받아 합을 구하는 예제(옳은 예시)
Num1 = int(input('첫 번째 숫자를 입력하세요 : '))  # int() 함수를 활용하여 정수형으로 변환하여 저장
Num2 = int(input('두 번째 숫자를 입력하세요 : '))
Num3 = Num1 + Num2
print("두수의 합 :",Num3,'\n')

































