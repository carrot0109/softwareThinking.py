'''표준 출력함수 print()'''

# 1.문자열 출력(큰 따옴표와 작은 따옴표 구분 없이 사용)
print("출력하고자 하는 문자열")
print('출력하고자 하는 문자열')

# 2.변수에 저장된 값 출력(따옴표 없이 변수명만 작성)
a = 100
print(a)
name = "유근오"
print(name)

# 3.변수에 저장된 값 + 문자열 혼합 출력(콤마와 플러스 기호 사용)
Name = "유근오"
univ = "국민대학교"
print(Name + "님은 " + univ + " 학생입니다")   # 플러스 사용시 띄어쓰기 X
print(Name,"님은",univ,"학생입니다")   # 콤마 사용시 띄어쓰기 O

# 3-1. 숫자 혼합 출력
grade = 3
# print(Name + "님은 " + grade + "학년 학생입니다")
# 숫자를 저장하고 있는 변수를 플러스 기호를 사용하여 출력 시 오류 발생
# 따라서 str()을 통해 문자열로 바꾸거나 콤마를 사용하여 출력
print(Name + "님은 " + str(grade) + "학년 학생입니다")
print(Name, "님은", grade, "학년 학생입니다",'\n')

